public class LiteralPratice{
    public static void main(String[] args){
        //long literal with 'L' suffix
        long Lnumber = 9804356333L;
        
        //float literal with 'f' suffix
        float fnumber = 10.55f;
        
        //char literal using Unicode escape ( Â© symbol = \u00A9 )
        char copyrightSymbol = '\u00A9';
        
        // printing values
        System.out.println(Lnumber);
        System.out.println(fnumber);
        System.out.println(copyrightSymbol);
    }
}
